#!/usr/bin/env tsx
/**
 * Skill mode verification — tests the full skill writer workflow:
 *
 *   1. Run deterministic pipeline through plan-report
 *   2. Export SkillWriterBundle
 *   3. Mock skill responses (simulate Claude Code generating prose)
 *   4. Import responses and assemble final report
 *   5. Validate the assembled report passes all checks
 *
 * Usage: npx tsx src/report/evals/runner/run-skill-mode.ts [fixture-id]
 */

import { writeFile, mkdir } from 'node:fs/promises';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

import type { Dossier } from '../../../types/index.js';
import {
  exportSkillBundle,
  writeReport,
} from '../stubs/stages.js';
import type { SkillSectionResponse, SkillWriterBundle } from '../../writer/types.js';
import {
  validateSkillResponses,
  sectionRequestToPrompt,
} from '../../writer/skill-writer.js';
import { loadText } from '../scoring/common.js';

// ---------------------------------------------------------------------------
// Paths
// ---------------------------------------------------------------------------

const __dirname = dirname(fileURLToPath(import.meta.url));
const FIXTURES_DIR = resolve(__dirname, '../fixtures');

// ---------------------------------------------------------------------------
// Mock prose generator
// ---------------------------------------------------------------------------

/**
 * Simulates what Claude Code would produce given a SkillWriterBundle.
 * Generates valid prose that passes all validation checks.
 */
function mockSkillResponses(bundle: SkillWriterBundle): SkillSectionResponse[] {
  const responses: SkillSectionResponse[] = [];

  for (const req of bundle.sections) {
    let markdown = '';

    switch (req.section_id) {
      case 'sec_01':
        markdown = `${req.report_context.core_thesis}\n\n`;
        if (req.relevant_implications.length > 0) {
          markdown += `The analysis surfaces several implications for stakeholders. `;
          markdown += `${req.relevant_implications[0].statement.split(/\.\s/)[0].replace(/\.+$/, '')}.\n`;
        }
        break;

      case 'sec_02':
        for (const sig of req.relevant_signals.slice(0, 6)) {
          markdown += `**${sig.title}.** ${sig.statement}\n\n`;
        }
        if (req.relevant_patterns.length > 0) {
          markdown += 'Several structural patterns emerge from these observations:\n\n';
          for (const pat of req.relevant_patterns) {
            markdown += `**${pat.title}.** ${pat.summary}\n\n`;
          }
        }
        break;

      case 'sec_03':
        for (const t of req.relevant_tensions) {
          markdown += `**${t.title}.** ${t.statement}\n\n`;
        }
        break;

      case 'sec_04':
        for (const h of req.relevant_hypotheses) {
          const phrase = req.style_rules.confidence_language[h.confidence];
          markdown += `**${h.title}.** ${phrase} ${h.statement.charAt(0).toLowerCase()}${h.statement.slice(1)}\n\n`;
          if (h.strongest_support && h.strongest_support.length > 0) {
            markdown += `This is supported by the following:\n\n`;
            for (const s of h.strongest_support.slice(0, 3)) {
              markdown += `- ${s}\n`;
            }
            markdown += '\n';
          }
          if (h.residual_uncertainty) {
            markdown += `However, ${h.residual_uncertainty.charAt(0).toLowerCase()}${h.residual_uncertainty.slice(1)}\n\n`;
          }
        }
        break;

      case 'sec_05':
        for (const imp of req.relevant_implications) {
          markdown += `**${imp.title}.** ${imp.statement}\n\n`;
          if (imp.key_questions.length > 0) {
            markdown += 'Key questions:\n\n';
            for (const q of imp.key_questions.slice(0, 3)) {
              markdown += `- ${q}\n`;
            }
            markdown += '\n';
          }
        }
        break;

      case 'sec_06':
        if (req.relevant_hypotheses.length === 0) {
          markdown = 'All hypotheses generated by the analysis survived stress testing. No major areas of analytical uncertainty were identified.\n';
        } else {
          markdown = 'The following hypotheses did not meet the threshold for confident inclusion in the analysis. They remain plausible but lack sufficient evidence.\n\n';
          for (const h of req.relevant_hypotheses) {
            markdown += `**${h.title}.** This may indicate ${h.statement.charAt(0).toLowerCase()}${h.statement.slice(1)}\n\n`;
            if (h.missing_evidence && h.missing_evidence.length > 0) {
              markdown += 'Evidence that would strengthen or refute this:\n\n';
              for (const m of h.missing_evidence.slice(0, 3)) {
                markdown += `- ${m}\n`;
              }
              markdown += '\n';
            }
          }
        }
        break;

      default:
        markdown = `This section presents analysis derived from the structured pipeline.`;
    }

    responses.push({ section_id: req.section_id, markdown: markdown.trim() });
  }

  // Add summary response
  const summaryParts: string[] = [];
  summaryParts.push(bundle.summary_request.core_thesis);
  summaryParts.push(`The analysis surfaces ${bundle.summary_request.key_findings.length} key findings.`);
  summaryParts.push(`This report is structured in ${bundle.summary_request.section_count} sections, progressing from observable evidence through tension analysis and hypothesis formation to strategic implications.`);
  responses.push({
    section_id: 'summary',
    markdown: summaryParts.join(' '),
  });

  return responses;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

async function main(fixtureId: string) {
  console.log(`\nSkill Mode Verification: ${fixtureId}`);
  console.log('='.repeat(50));

  // Load dossier
  const dir = resolve(FIXTURES_DIR, fixtureId);
  const dossierRaw = await loadText(resolve(dir, 'dossier.json'));
  const dossier: Dossier = JSON.parse(dossierRaw);

  // Step 1: Export skill bundle (Path A)
  console.log('\n1. Exporting skill bundle...');
  const { bundle } = exportSkillBundle(dossier);
  console.log(`   Report ID: ${bundle.report_id}`);
  console.log(`   Company ID: ${bundle.company_id}`);
  console.log(`   Sections: ${bundle.sections.length}`);
  for (const sec of bundle.sections) {
    console.log(`     ${sec.section_id}: "${sec.title}" (${sec.relevant_hypotheses.length}h, ${sec.relevant_implications.length}i, ${sec.relevant_patterns.length}p, ${sec.relevant_tensions.length}t, ${sec.relevant_signals.length}s)`);
  }

  // Step 2: Show example prompt for first section
  console.log('\n2. Example prompt for sec_01:');
  const examplePrompt = sectionRequestToPrompt(bundle.sections[0]);
  console.log(`   (${examplePrompt.length} chars)`);
  console.log(`   First 200 chars: ${examplePrompt.substring(0, 200)}...`);

  // Step 3: Generate mock skill responses
  console.log('\n3. Generating mock skill responses...');
  const responses = mockSkillResponses(bundle);
  for (const r of responses) {
    console.log(`   ${r.section_id}: ${r.markdown.length} chars`);
  }

  // Step 4: Validate skill responses against bundle
  console.log('\n4. Validating skill responses against bundle...');
  const validationErrors = validateSkillResponses(bundle, responses.filter(r => r.section_id !== 'summary'));
  if (validationErrors.length > 0) {
    console.log('   VALIDATION ERRORS:');
    for (const err of validationErrors) {
      console.log(`     - ${err}`);
    }
  } else {
    console.log('   All sections fulfilled.');
  }

  // Step 5: Assemble final report using skill mode (Path B)
  console.log('\n5. Assembling report from skill responses...');
  const result = await writeReport(dossier, {
    writerMode: 'skill',
    skillResponses: responses,
  });

  if (result.errors.length > 0) {
    console.log('   REPORT VALIDATION ERRORS:');
    for (const err of result.errors) {
      console.log(`     [${err.check}] ${err.message}`);
    }
  } else {
    const report = result.report!;
    console.log(`   Sections: ${report.sections.length}`);
    console.log(`   Summary: ${report.summary.length} chars`);
    console.log(`   Markdown: ${result.markdown.length} chars`);
    for (const sec of report.sections) {
      const lineageCount = sec.hypothesis_ids.length + sec.pattern_ids.length +
        sec.tension_ids.length + sec.signal_ids.length;
      console.log(`     ${sec.section_id}: ${sec.title} (${sec.markdown.length} chars, ${lineageCount} lineage refs)`);
    }
  }

  // Step 6: Also verify template mode still works
  console.log('\n6. Verifying template mode unchanged...');
  const templateResult = await writeReport(dossier);
  if (templateResult.errors.length > 0) {
    console.log('   TEMPLATE MODE ERRORS:');
    for (const err of templateResult.errors) {
      console.log(`     [${err.check}] ${err.message}`);
    }
  } else {
    console.log('   Template mode: PASS');
  }

  // Step 7: Write bundle and responses as example artifacts
  const artifactsDir = resolve(dir, 'skill-mode-artifacts');
  await mkdir(artifactsDir, { recursive: true });
  await writeFile(
    resolve(artifactsDir, 'skill-bundle.json'),
    JSON.stringify(bundle, null, 2),
  );
  await writeFile(
    resolve(artifactsDir, 'skill-responses.json'),
    JSON.stringify(responses, null, 2),
  );
  if (result.report) {
    await writeFile(
      resolve(artifactsDir, 'skill-report.json'),
      JSON.stringify(result.report, null, 2),
    );
  }
  await writeFile(
    resolve(artifactsDir, 'skill-report.md'),
    result.markdown,
  );
  console.log(`\n7. Artifacts written to: ${artifactsDir}`);

  const allPassed = result.errors.length === 0 && templateResult.errors.length === 0 && validationErrors.length === 0;
  console.log(`\nOverall: ${allPassed ? 'PASS' : 'FAIL'}\n`);
}

const fixtureId = process.argv[2] ?? '001-ai-services';

main(fixtureId).catch(err => {
  console.error('Skill mode verification failed:', err);
  process.exit(1);
});
